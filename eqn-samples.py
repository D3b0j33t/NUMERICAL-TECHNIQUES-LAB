# 1. Simple System of Two Equations (Linear and Nonlinear)
equations_1 = [
    "x1**2 + x2**2 - 9",  # x1^2 + x2^2 - 9 = 0
    "x1 + x2 - 1"          # x1 + x2 - 1 = 0
]

# 2. A System of Two Equations (Nonlinear)
equations_2 = [
    "x1**3 + x1*x2**2 - 1",  # x1^3 + x1*x2^2 - 1 = 0
    "x1**2 - x2**2 - 1"      # x1^2 - x2^2 - 1 = 0
]

# 3. System of Three Equations (Nonlinear)
equations_3 = [
    "x1**2 + x2**2 + x3**2 - 4",  # x1^2 + x2^2 + x3^2 - 4 = 0
    "x1 - x2 + 1",                # x1 - x2 + 1 = 0
    "x3 - x1*x2"                  # x3 - x1*x2 = 0
]

# 4. System of Two Equations with Exponentials
equations_4 = [
    "exp(x1) + x2 - 3",  # e^x1 + x2 - 3 = 0
    "x1**2 + x2**2 - 4"   # x1^2 + x2^2 - 4 = 0
]

# 5. System with a Trigonometric Equation
equations_5 = [
    "sin(x1) + x2 - 1",  # sin(x1) + x2 - 1 = 0
    "x1**2 - x2"          # x1^2 - x2 = 0
]

# 6. A System of Nonlinear Equations Involving Polynomials and Roots
equations_6 = [
    "x1**2 + 2*x2 - 3",     # x1^2 + 2*x2 - 3 = 0
    "sqrt(x1) - x2 - 1"     # sqrt(x1) - x2 - 1 = 0
]

# 7. A System of Exponential and Polynomial Equations
equations_7 = [
    "exp(x1) + x2 - 5",      # e^x1 + x2 - 5 = 0
    "x1**3 - x2**2 + 1"      # x1^3 - x2^2 + 1 = 0
]

# 8. System with Two Equations Representing a Hyperbola and a Circle
equations_8 = [
    "1/x1**2 + x2**2 - 1",    # 1/x1^2 + x2^2 - 1 = 0
    "x1**2 + x2**2 - 4"       # x1^2 + x2^2 - 4 = 0
]

